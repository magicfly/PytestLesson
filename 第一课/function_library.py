def add(a,b):
    """@brief add two numbers
    @param a : one number
    @param b : another number
    @return : sum of two numbers
    """
    return a+b
def subtract(a,b):
    """@brief subtract two numbers
    @param a : one number
    @param b : another number
    @return : sum of two numbers
    """
    return a-b